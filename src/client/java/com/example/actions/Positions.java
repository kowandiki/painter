	public static final int NORTH = 0;
	public static final int SOUTH = 1;
	public static final int EAST = 2;
	public static final int WEST = 3;
	
	public static int getPlayerCardinality()
	{
		float yaw = Math.abs(FabricPainterClient.mc.player.getYaw()) % 360;
		
		// will offer priority to north/south even though it will likely never come up
		if (yaw >= 135 && yaw <= 225)
			return NORTH;
		if (yaw < 135 && yaw > 45)
			return WEST;
		if (yaw > 225 && yaw < 315)
			return EAST;
		
		return SOUTH;
	}
	
	
	/*
	 * given the row and column gets the associated yaw and pitch for that pixel
	 * needs the cardinal direction the player is facing (Positions.NORTH, SOUTH, EAST, WEST)
	 */
	public static LookDir getOrientation(int row, int column, int cardinal)
	{
		if (row > 31 || row < 0 || column > 31 || column < 0)
		{
			System.out.println("row or column invalid" + row  + " " + column);
			return null;
		}
		
		switch(cardinal)
		{
		case Positions.NORTH:
			return North[row][column];
		case Positions.SOUTH:
			return new LookDir(North[row][column].yaw + 180, North[row][column].pitch);
		case Positions.EAST:
			return new LookDir(North[row][column].yaw + 90, North[row][column].pitch);
		case Positions.WEST:
			return new LookDir(North[row][column].yaw - 90, North[row][column].pitch);
		default:
			System.out.println("invalid cardinal");
			return null;
		}
	}
	
